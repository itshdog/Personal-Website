function contactBlur() {
    var x = document.getElementById("contactWrapper");
    x.className = "hidden"
    x.style.opacity = "0";
}

function contactOpen() {
    var x = document.getElementById("contactWrapper");
    x.className = ""
    x.style.opacity = "1";
}

